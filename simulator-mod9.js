function logMsg(msg, isSuccess = false) { return `<div style="margin-bottom: 5px; color: ${isSuccess ? '#4ade80' : '#f87171'}">${isSuccess ? '✅' : '❌'} ${msg}</div>`; }

function runR1HsrpTest() {
    let R1 = network.R1; let pass = true; let output = `<b style="color:white; font-size:1.1rem; display:block;">Checking R1 HSRP (Active)</b>`;

    let g1 = R1.interfaces['g0/1'] || { hsrpVersion: 1, hsrpGroups: {} };
    if (g1.hsrpVersion === 2) output += logMsg(`HSRP Version 2 enabled on g0/1.`, true); else { output += logMsg(`HSRP Version is still 1. Use 'standby version 2'.`); pass = false; }
    
    let grp = g1.hsrpGroups[1];
    if (grp) {
        output += logMsg(`HSRP Group 1 created.`, true);
        if (grp.vip === '192.168.1.254') output += logMsg(`Virtual IP correctly set to 192.168.1.254.`, true); else { output += logMsg(`Virtual IP missing or incorrect.`); pass = false; }
        if (grp.priority === 150) output += logMsg(`Priority correctly elevated to 150.`, true); else { output += logMsg(`Priority is not 150. R1 will not become Active!`); pass = false; }
        if (grp.preempt) output += logMsg(`Preempt enabled. R1 will seize control.`, true); else { output += logMsg(`Preempt missing! R1 won't take back over if it reboots.`); pass = false; }
    } else { output += logMsg(`HSRP Group 1 is missing on g0/1.`); pass = false; }

    output += pass ? `<br><strong style="color:#4ade80;">RESULT: R1 HSRP CONFIGURED PERFECTLY!</strong>` : `<br><strong style="color:#f87171;">RESULT: ERRORS DETECTED. Review hints above.</strong>`;
    diagOutput.innerHTML = output; diagOutput.className = pass ? 'diag-success' : 'diag-error';
    highlightElement(diagOutput);
}

