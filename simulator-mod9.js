function runFailoverTest() {
    let R1 = network.R1; let R3 = network.R3; let pass = true; let output = `<b style="color:white; font-size:1.1rem; display:block;">Checking FHRP Failover Logic</b>`;
    
    let r1Grp = (R1.interfaces['g0/1'] || {hsrpGroups:{}}).hsrpGroups[1];
    let r3Grp = (R3.interfaces['g0/0'] || {hsrpGroups:{}}).hsrpGroups[1];

    if (!r1Grp || !r3Grp) {
        diagOutput.innerHTML = `<strong style="color:#f87171;">ERROR: Finish configuring both R1 and R3 first!</strong>`;
        diagOutput.className = 'diag-error'; return;
    }

    if (r1Grp.vip === r3Grp.vip && r1Grp.vip === '192.168.1.254') output += logMsg(`Virtual IP matches across both routers.`, true); else { output += logMsg(`Virtual IPs do not match! Split-brain scenario detected.`); pass = false; }
    if (r1Grp.priority > r3Grp.priority) output += logMsg(`R1 Priority (${r1Grp.priority}) > R3 Priority (${r3Grp.priority}). R1 is ACTIVE.`, true); else { output += logMsg(`R1 priority is not higher than R3. R1 is NOT active.`); pass = false; }
    if (r1Grp.preempt) output += logMsg(`R1 Preempt enabled. Redundancy loop secured.`, true); else { output += logMsg(`R1 Preempt missing. Failback will break.`); pass = false; }

    output += pass ? `<br><strong style="color:#4ade80;">RESULT: HIGH AVAILABILITY NETWORK ACHIEVED!</strong>` : `<br><strong style="color:#f87171;">RESULT: REDUNDANCY FAILURE. Review hints above.</strong>`;
    diagOutput.innerHTML = output; diagOutput.className = pass ? 'diag-success' : 'diag-error';
    highlightElement(diagOutput);
}

